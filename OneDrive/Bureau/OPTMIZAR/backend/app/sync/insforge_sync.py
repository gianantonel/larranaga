def sync_to_insforge(sql_content: str, retry_attempts: int = 3) -> bool:
    """Sincroniza el SQL a InsForge con reintentos."""

    logger.info(f"Iniciando sincronización a InsForge ({INSFORGE_API_URL})")

    headers = {
        "Authorization": f"Bearer {INSFORGE_API_KEY}"
    }

    for attempt in range(1, retry_attempts + 1):
        try:
            # Guardar SQL en archivo temporal
            import tempfile
            with tempfile.NamedTemporaryFile(mode='w', suffix='.sql', delete=False) as f:
                f.write(sql_content)
                temp_file = f.name

            # Upload
            with open(temp_file, 'rb') as f:
                files = {"file": ("larranaga_sync.sql", f)}
                data = {"truncate": "false"}

                logger.info(f"Intento {attempt}/{retry_attempts}: POST {INSFORGE_IMPORT_ENDPOINT}")
                response = requests.post(
                    INSFORGE_IMPORT_ENDPOINT,
                    headers=headers,
                    files=files,
                    data=data,
                    timeout=120
                )

            # Limpiar
            os.unlink(temp_file)

            if response.status_code < 400:
                result = response.json()
                logger.info(f"Sincronización exitosa!")
                logger.info(f"  Tablas: {result.get('tables')}")
                logger.info(f"  Registros: {result.get('rowsImported')}")
                return True
            else:
                logger.warning(f"Intento {attempt} falló: {response.status_code} - {response.text[:200]}")
                if attempt < retry_attempts:
                    import time
                    time.sleep(2 ** attempt)  # Backoff exponencial

        except Exception as e:
            logger.error(f"Intento {attempt} error: {e}")
            if attempt < retry_attempts:
                import time
                time.sleep(2 ** attempt)

    logger.error(f"Sincronización falló después de {retry_attempts} intentos")
    return False


