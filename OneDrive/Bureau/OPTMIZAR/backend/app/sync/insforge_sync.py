def export_database_to_sql(db_path: str) -> str:
    """Exporta la base de datos SQLite a SQL compatible con PostgreSQL/InsForge."""

    logger.info(f"Exportando base de datos desde {db_path}")

    try:
        conn = sqlite3.connect(db_path)
        conn.row_factory = sqlite3.Row
        cursor = conn.cursor()

        # Obtener todas las tablas
        cursor.execute("SELECT name FROM sqlite_master WHERE type='table';")
        tables = [row[0] for row in cursor.fetchall()]

        database_dump = {}
        for table in tables:
            cursor.execute(f"SELECT * FROM {table}")
            columns = [description[0] for description in cursor.description]
            rows = cursor.fetchall()

            table_data = []
            for row in rows:
                row_dict = dict(zip(columns, row))
                table_data.append(row_dict)

            database_dump[table] = table_data
            logger.info(f"  Tabla {table}: {len(table_data)} registros")

        conn.close()

        # Generar SQL
        sql_output = generate_sql_from_dump(database_dump)
        logger.info(f"SQL generado: {len(sql_output)} bytes")

        return sql_output

    except Exception as e:
        logger.error(f"Error exportando base de datos: {e}")
        raise


